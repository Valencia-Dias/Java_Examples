package oops;

public class classes {
    int x = 2;
    int a;
    int b = 19;
    String s = "val";
    Boolean t = true;


    public static void main(String[] args) {
        classes obj = new classes();
        obj.a = 4;
        obj.b = 20;
        System.out.println(obj.x);
        System.out.println(obj.s);
        System.out.println(obj.t);
        System.out.println(obj.a);
        System.out.println(obj.b);

    }
    
}
